      <b><p>Cómo funciona <br></b>
       1. Una celda de carga OEM LSM250 o LSM300 está montada dentro de una unidad de mano con una punta penetrante adjunta.<br>
       2.Luego, un operador presiona el ensamblaje en la pulpa de la fruta o verdura.<br>
       3.La celda de carga mide la fuerza aplicada a lo largo de todo el proceso que se envía a la pantalla portátil digital IHH500
        o la solución USB de alta resolución USB220 conectada a una computadora portátil que ejecuta nuestro software SENSIT.<br>
       4.El IHH500 y el USB220 rastrean la fuerza aplicada y muestran la carga máxima que se aplicó.<br>
       5.Luego, la carga máxima se correlaciona con las tablas para determinar la madurez de la muestra.<br>
       6.El LSM250 y el LSM300 brindan hasta un 1000% de protección de sobrecarga axial (2.2 lb a 25 lb) para mitigar el error del
